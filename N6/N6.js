const age = '18';
// Считает конкатенацией строк
console.log(age + 5);
console.log(Number(age) + 5);
// Считает арифметической операцией
console.log(age - 5);
console.log(age / 5);
console.log(age * 5);

const userName = 'Вася';
console.log(Number(userName) + 5);
console.log(typeof NaN);
console.log(String(4) + 7);

// В булевых выражениях все числа кроме 0 = true
console.log(Boolean('-1'));
// 0(false) + 10 = 10
console.log(Boolean('') + 10);
// 1(true) + 2 = 3
console.log(true + 2);
console.log(Boolean(0));
